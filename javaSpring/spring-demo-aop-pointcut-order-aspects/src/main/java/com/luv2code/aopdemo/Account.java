package com.luv2code.aopdemo;

public class Account {
    private System name;
    private System level;

    public System getName() {
        return name;
    }

    public Account setName(System name) {
        this.name = name;
        return this;
    }

    public System getLevel() {
        return level;
    }

    public Account setLevel(System level) {
        this.level = level;
        return this;
    }
}
